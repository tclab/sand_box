    /**
     * Guardar edicion de concepto.
     * @param evt
     */
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        String concepto = jTextField1.getText();
        String significado = jTextArea1.getText();
        String teman = jComboBox1.getSelectedItem().toString();

        if ((concepto.equals(conEditar.getConcepto())) && (significado.equals(conEditar.getSignificado())) 
                && (teman.equals(conEditar.getTema()))) {

            JOptionPane.showConfirmDialog(jPanel3, "No se ha modificado el concepto." +
                    "\nVerifique los cambios e intente de nuevo.", "Error", -1, JOptionPane.ERROR_MESSAGE);

        } else {
            ConceptoBO conNuevo = new ConceptoBO();

            conNuevo.setConcepto(concepto);
            conNuevo.setSignificado(significado);
            conNuevo.setTema(teman);

            if(conceptoDao.modificarConcepto(conNuevo, conEditar)){
                index.llenarConceptos(conEditar.getTema());
                this.dispose();
            }else{
                JOptionPane.showConfirmDialog(jPanel3, "Ocurrio un error modificando el " +
                        "concepto.", "Error", -1, JOptionPane.ERROR_MESSAGE);
            }
        }

}//GEN-LAST:event_jButton3ActionPerformed

