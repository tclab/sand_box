    /**
     * Llena la lista de temas de conceptos.
     */
    private void llenarTemas() {
        Vector<TemaBO> temas = new Vector();

        temas = temaDao.getTemas();

        for (int i = 0; i < temas.size(); i++) {
            jComboBox1.addItem(temas.elementAt(i).getDescripcion());
        }
    }

