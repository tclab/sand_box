package com.enciclopedia.beans;

public class Concepto {
	
	private String cdConcepto;

	private String dsConcepto;
	
	private String adjunto;
	
		
	public Concepto(){
		
	}
	
	
	public String getCdConcepto() {
		return cdConcepto;
	}

	public void setCdConcepto(String cdConcepto) {
		this.cdConcepto = cdConcepto;
	}

	public String getDsConcepto() {
		return dsConcepto;
	}

	public void setDsConcepto(String dsConcepto) {
		this.dsConcepto = dsConcepto;
	}

	public String getAdjunto() {
		return adjunto;
	}

	public void setAdjunto(String adjunto) {
		this.adjunto = adjunto;
	}

}
